    /// Primary action button (blue).
    /// </summary>
    Primary,

    /// <summary>
    /// Secondary action button (gray).
    /// </summary>
    Secondary,

    /// <summary>
    /// Destructive action button (red).
    /// </summary>
    Danger,

    /// <summary>
    /// Transparent button (text only).
    /// </summary>
    Ghost,
}
